    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // TODO add your handling code here:
        try {
            
            String CampoUser, CampoSenha;
            
            CampoUser = txtCampoUser.getText();
            CampoSenha = txtCampoSenha.getText(); 
            
            LoginDTO objlogindto = new LoginDTO();
            objlogindto.setNome_usuario(CampoUser);
            objlogindto.setSenha_usuario(CampoSenha);
            
            LoginDAO objlogindao = new LoginDAO();
            ResultSet rslogindao = objlogindao.autenticarLogin(objlogindto);
            
            if(rslogindao.next()) {
                TelaMain objtelamainview = new TelaMain();
                objtelamainview.setVisible(true);
                
                dispose();
            }else{
                JOptionPane.showMessageDialog(null, "Usuario ou senha invalidos.");
            }
            
        }
        catch (SQLException erro) {
            JOptionPane.showMessageDialog(null, "Tela Login: " + erro);
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(TelaLogin.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnLoginActionPerformed

