    private void listarValores() throws ClassNotFoundException{
        try {
            ProdutoDAO objprodutodao = new ProdutoDAO();
            
            DefaultTableModel model = (DefaultTableModel) tabela.getModel();
            model.setNumRows(0);
            
            ArrayList<ProdutoDTO> lista = objprodutodao.PesquisarProduto();
            
            for(int num = 0 ; num < lista.size(); num ++){
                model.addRow(new Object[]{
                   lista.get(num).getCodigo_produto(),
                   lista.get(num).getNome_produto(),
                   lista.get(num).getQuantidade_produto(),
                });
            }
        } catch (Exception erro) {
            JOptionPane.showMessageDialog(null, "ListarValores: " + erro);
        }
        
    }

